using System;

namespace MultiClicksTask {

    public class MultiClicksBlock {

        public float mX;
        public float mY;
        public float mWidth;
        public float mHeight;
        public int mTexture;

        public MultiClicksBlock(float x, float y, float width, float height) {
	        mX = x;
	        mY = y;
	        mWidth = width;
	        mHeight = height;
	        mTexture = 0;
        }
    
    }

}
